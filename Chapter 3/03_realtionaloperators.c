//Realtion operators

#include <stdio.h>

int main() {
    int a=2,b=15;
    printf("%d \n",a>b);//greater than operator = false(0)
    printf("%d \n",a<b);//less than operator = true (non zero)
    printf("%d \n",a==b);//equal to operator =false(0)
    printf("%d \n",a!=b);//not equal to  operator =true(non zero)
    printf("%d \n",a>=b);//greater than equal operator false(0)
    printf("%d \n",a<=b);//less  than equal to operator =true(non zero)
    
     
    return 0;
}